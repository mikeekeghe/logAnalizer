    @FXML
    private boolean onClickedAnalyzeBtn(ActionEvent event) {
        String input = txtInput.getText();
        
        if ((input.trim().length() == 0) || (input == "") || (input.trim().isEmpty())) {
            
            Alert alert1 = new Alert(Alert.AlertType.INFORMATION);
            alert1.setTitle("Information Dialog");
            alert1.setHeaderText(null);
            alert1.setContentText("Input is Invalid.");
            
            alert1.showAndWait();
            
            return false;
            
        } else {
            makeArrayList();
            //loop through accountList
            for (String temp : accountList) {
                //check if input contains accountList element
                if (input.contains(temp)) {
                    //if it contains write and iterate
                    counter++;
                    txtOutput.appendText(temp);
                    txtOutput.appendText("\n");
                    System.out.println(temp);
                }
                
            }
            
        }
        txtInput.setText("");
        txtCount.setText(counter + "");
           
            Alert alert1 = new Alert(Alert.AlertType.INFORMATION);
            alert1.setTitle("Information Dialog");
            alert1.setHeaderText(null);
            alert1.setContentText("Operation Completed.");
            
            alert1.showAndWait();
        return true;
    }
    
